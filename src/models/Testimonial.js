const mongoose = require("mongoose");

const TestimonialSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true
    },
    message: {
      type: String,
      required: true
    },
    role: {
      type: String,
      default: ""
    },
    rating: {
      type: Number,
      min: 1,
      max: 5,
      default: 5
    },
    image: {
      type: String,
      default: null // URL of image
    }
  },
  { timestamps: true }
);

module.exports = mongoose.model("Testimonial", TestimonialSchema);
